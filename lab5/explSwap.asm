.data
    number DB 0b10100101  ; Исходное число (пример: 10100101b)

.code
main PROC
    ; Загружаем значение из памяти в регистр AL
    mov al, number

    ; Создаем маски для 2-го и 3-го битов
    ; 2-й бит: 00000100 (0x04)
    ; 3-й бит: 00000010 (0x02)
    mov bl, al          ; Сохраняем исходное значение в BL

    ; Извлекаем биты
    and al, 00000100b  ; Получаем 2-й бит
    shr al, 2           ; Сдвигаем его на место 0
    and bl, 00000010b  ; Получаем 3-й бит
    shr bl, 1           ; Сдвигаем его на место 0

    ; Теперь AL содержит 2-й бит на месте 0, а BL - 3-й бит на месте 0
    ; Объединяем обратно с оригинальным числом
    ; Убираем старые биты из оригинала
    and number, 0b11111011  ; Убираем 2-й бит (устанавливаем его в 0)
    and number, 0b11111101  ; Убираем 3-й бит (устанавливаем его в 0)

    ; Теперь добавляем обратно биты на их новые места
    or number, al         ; Добавляем новый 2-й бит
    shl bl, 1             ; Сдвигаем новый 3-й бит на место
    or number, bl         ; Добавляем новый 3-й бит

    ; Завершение программы
    ret
main ENDP
END main
